#include "checker.h"

int	main(int ac, char **av)
{
	t_stack	*a;
	t_stack	*b;

	a = NULL;
	b = NULL;
	if (ac == 1 || (ac == 2 && !av[1][0]))
		return (1);
	create_stack(&a, ac, av);
	check_moves(&a, &b);
	free_stack(&a);
}
