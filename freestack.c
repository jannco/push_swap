#include "push_swap.h"

void	free_stack(t_stack **a)
{
	t_stack	*tmp;

	while (*a)
	{
		tmp = (*a)->next;
		free((*a));
		*a = tmp;
	}
	free(*a);
	*a = NULL;
}
